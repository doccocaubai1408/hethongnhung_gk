// adc 
uint16_t  adc_values;
float vol ;
uint16_t get_adc()
{
	uint16_t adc;
HAL_ADC_Start(&hadc1);
	HAL_ADC_PollForConversion(&hadc1,1000);
	adc=HAL_ADC_GetValue(&hadc1);
	return adc;
	HAL_ADC_Stop(&hadc1);
}

void adc_to_vol( )
{
	adc_values=get_adc();
	vol= (((float)adc_values-0)*(3.3 - 0))/(4095-0) +0 ;
}


//i2c lcd 

#define diachia_lcd  0x4E
int hang=0,cot=0;
void gui_lenh_lcd(char lenh)
{
	// xxxx bl en rw rs 
char bit_cao,bit_thap;
	uint8_t bit_gui[4];
	bit_cao = (lenh & 0xf0);
	bit_thap = ( (lenh<<4) & 0xf0);
	bit_gui[0] = bit_cao  | 0x0C; 
	bit_gui[1] = bit_cao  | 0x08;
	bit_gui[2] = bit_thap | 0x0C;
	bit_gui[3] = bit_thap | 0x08;
	HAL_I2C_Master_Transmit (&hi2c1, diachia_lcd,(uint8_t *) bit_gui, 4, 100);
}
void gui_data_lcd(char data )
{
	// xxxx bl en rw rs 
char bit_cao,bit_thap;
	uint8_t bit_gui[4];
	bit_cao = (data  & 0xf0);
	bit_thap = ( (data <<4) & 0xf0);
	bit_gui[0] = bit_cao  | 0x0D; //xxxx 1101 
	bit_gui[1] = bit_cao  | 0x09; // xxxx 1001
	bit_gui[2] = bit_thap | 0x0D;
	bit_gui[3] = bit_thap | 0x09;
	HAL_I2C_Master_Transmit (&hi2c1, diachia_lcd,(uint8_t *) bit_gui, 4, 100);
}
void xoa_lcd  (void)
{
	gui_lenh_lcd (0x80);
	for (int i=0; i<70; i++)
	{
		gui_data_lcd (' ');
	}
}
void vi_tri(int hang,int cot)
{
	switch (hang)
	{
		case 0:
			cot |= 0x80;
break;		
		case 1:
			cot |= 0xC0;
break;
		}
	gui_lenh_lcd(cot);
}
void thiet_lap_lcd()
{
HAL_Delay(50);
// 8bit mode
gui_lenh_lcd(0x30);
HAL_Delay(5);
gui_lenh_lcd(0x30);
HAL_Delay(1);	
gui_lenh_lcd(0x30);
HAL_Delay(10);
// 4bit mode
gui_lenh_lcd(0x20);
HAL_Delay(10);
gui_lenh_lcd(0x28);
HAL_Delay(1);
gui_lenh_lcd(0x08);
	HAL_Delay(1);
gui_lenh_lcd(0x01);
	HAL_Delay(1);
	HAL_Delay(1);
	gui_lenh_lcd(0x06);
	HAL_Delay(1);
	gui_lenh_lcd(0x0C);
}
 void lcd_in_chuoi  (char *str)
{
	while (*str) gui_data_lcd (*str++);
}

